package org.comit.course._02_operators;

public class _02_Relational {
	public static void main(String[] args) {
		int a= 10;
		int b =8;
		
		boolean equalsTo = a==b;
		boolean notequals = a!=b;
		boolean greaterThan = a>b;
		
		System.out.println("EqualsTo: " + equalsTo);
		System.out.println("Not Equal To: " + notequals);
		System.out.println("Greater than: " + greaterThan);
		
	}

}
